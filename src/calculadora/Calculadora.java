package calculadora;

public class Calculadora {
	public int s(int num1, int num2){
		return num1+num2;
		
	}
	public int r(int num1, int num2){
		return num1-num2;
		
	}
	public int m(int num1, int num2){
		return num1*num2;
	
}
	public int d(int num1, int num2){
		return num1/num2;
	
}
}
